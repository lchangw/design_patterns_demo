package builder.v1;

public class Person {

	private double headSize;
	private double bodySize;
	private double armSize;
	private double legSize;
	public double getHeadSize() {
		return headSize;
	}
	public void setHeadSize(double headSize) {
		this.headSize = headSize;
	}
	public double getBodySize() {
		return bodySize;
	}
	public void setBodySize(double bodySize) {
		this.bodySize = bodySize;
	}
	public double getArmSize() {
		return armSize;
	}
	public void setArmSize(double armSize) {
		this.armSize = armSize;
	}
	public double getLegSize() {
		return legSize;
	}
	public void setLegSize(double legSize) {
		this.legSize = legSize;
	}
	
	
}
